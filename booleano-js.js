const numero = 10;

// Está falando que numero divisivel por 2 é par
const numeroPar = numero % 2 === 0;

if(numeroPar) {
    console.log('o numero é par');
} else{
    console.log('o número é impar');
}

// O console.log está pegando o resto de cada operação, se o resto for 0 é par.
console.log(numeroPar);










